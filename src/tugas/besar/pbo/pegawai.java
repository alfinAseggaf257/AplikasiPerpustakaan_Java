    private void tbl_pegawaiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_pegawaiMouseClicked
        int baris = tbl_pegawai.rowAtPoint(evt.getPoint());
        String kode_buku =tbl_pegawai.getValueAt(baris, 0).toString();
        txt_nip_pegawai.setText(kode_buku);
        String judul_buku = tbl_pegawai.getValueAt(baris,1).toString();
        txt_nama_pegawai.setText(judul_buku);
        String jenkel_pegawai = tbl_pegawai.getValueAt(baris, 2).toString();
        cmb_jenkel_pegawai.setSelectedItem(jenkel_pegawai);
        String notelp_pegawai=tbl_pegawai.getValueAt(baris, 3).toString();
        txt_notelp_pegawai.setText(notelp_pegawai);
        String alamat_pegawai=tbl_pegawai.getValueAt(baris, 4).toString();
        txt_alamat_pegawai.setText(alamat_pegawai);
    }//GEN-LAST:event_tbl_pegawaiMouseClicked


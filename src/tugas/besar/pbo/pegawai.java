    private void tampildata(){
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("NIP ");
        model.addColumn("Nama");
        model.addColumn("Jenis Kelamin");
        model.addColumn("Nomor Telp");
        model.addColumn("Alamat");
        tbl_pegawai.setModel(model);
        try {            
            String sql = "SELECT * FROM pegawai";
            java.sql.Connection conn=(Connection)Koneksi.KoneksiDB.configDB();
            java.sql.Statement stm=conn.createStatement();
            java.sql.ResultSet res=stm.executeQuery(sql);
            while (res.next()) {                
                Object[] obj = new Object[5];
                obj[0] = res.getString("nip_pegawai"); 
                obj[1] = res.getString("nama_pegawai");
                obj[2] = res.getString("jenkel_pegawai");
                obj[3] = res.getString("notelp_pegawai");
                obj[4] = res.getString("alamat_pegawai");

                model.addRow(obj);
            }
            tbl_pegawai.setModel(model);
            //s.close();
            stm.close();
        } catch (SQLException ex) {
            Logger.getLogger(pegawai.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
  


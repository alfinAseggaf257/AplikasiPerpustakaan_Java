    private void tbl_bukuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_bukuMouseClicked
        int baris = tbl_buku.rowAtPoint(evt.getPoint());
        String kode_buku =tbl_buku.getValueAt(baris, 0).toString();
        txt_kode_buku.setText(kode_buku);
        String judul_buku = tbl_buku.getValueAt(baris,1).toString();
        txt_judul_buku.setText(judul_buku);
        String pengarang = tbl_buku.getValueAt(baris, 2).toString();
        txt_pengarang.setText(pengarang);
        String thn_terbit=tbl_buku.getValueAt(baris, 3).toString();
        txt_thn_terbit.setText(thn_terbit);
    }//GEN-LAST:event_tbl_bukuMouseClicked


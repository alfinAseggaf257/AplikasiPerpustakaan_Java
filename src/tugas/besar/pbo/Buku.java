    private void tampildata(){
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Kode Buku");
        model.addColumn("Judul Buku");
        model.addColumn("Pengarang");
        model.addColumn("Tahun Terbit");
        tbl_buku.setModel(model);
        try {            
            String sql = "SELECT * FROM buku";
            java.sql.Connection conn=(Connection)Koneksi.KoneksiDB.configDB();
            java.sql.Statement stm=conn.createStatement();
            java.sql.ResultSet res=stm.executeQuery(sql);
            while (res.next()) {                
                Object[] obj = new Object[4];
                obj[0] = res.getString("kode_buku"); 
                obj[1] = res.getString("judul_buku");
                obj[2] = res.getString("pengarang");
                obj[3] = res.getString("thn_terbit");

                
                model.addRow(obj);
            }
            tbl_buku.setModel(model);
            //s.close();
            stm.close();
        } catch (SQLException ex) {
            Logger.getLogger(Buku.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
